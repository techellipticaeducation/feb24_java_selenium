package feb24_java_selenium;

public class OperatorDemo {
public static void main(String[] args) {
	// operand 10 5
	// operator +
	// 10 + 5 
	
	// Arithmatic Operator
	// + , - , * , / , %
	
	int i = 10;
	int j = 7;
	
	System.out.println(i+j); // 17
	System.out.println(i-j); // 3
	System.out.println(i*j); // 70
	System.out.println(i/j); // 1.3433, 1
	System.out.println(i%j); // 3 
	
	int m1 = 10;
	int m2 = 7;
	
	// float > int
	
	float m3 = (float)m1/m2;
	System.out.println(m3);
	
	
	
	// odd and even 
	// number % 2 == 1 (odd) , == 0 = even
	
	
	
}
}
