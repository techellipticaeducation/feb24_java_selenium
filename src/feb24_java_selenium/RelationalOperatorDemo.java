package feb24_java_selenium;

public class RelationalOperatorDemo {
public static void main(String[] args) {
	// > , >= , < , <= , == , !=
	// generate boolean values
	int i = 10;
	int j = 10;
	
	System.out.println(i > j); // false
	System.out.println(i >= j); // true
	System.out.println(i < j); // false
	System.out.println(i <= j); // true
	System.out.println(i == j); // true
	System.out.println(i != j); // false
}
}
