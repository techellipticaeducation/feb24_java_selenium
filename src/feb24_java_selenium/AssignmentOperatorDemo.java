package feb24_java_selenium;

public class AssignmentOperatorDemo {
	public static void main(String[] args) {
		int i = 10;
		i+=3; // i =  i +3
		i-=3; // i =  i -3
		i*=3; // i =  i *3
		i/=3; // i =  i /3
		i%=3; // i =  i %3

		System.out.println(i);
		
		int j = 10 + 12 * 2 / 2;
		   // Precedence
		System.out.println(j);
		
	}
}
